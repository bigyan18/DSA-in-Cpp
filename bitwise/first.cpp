#include<iostream>
using namespace std;
int main(){
    int a=4,b=3;
    cout<< (a & b)<<endl;//and operator  
    cout<< (a|b)<<endl; //or operator
    cout<<(a^b)<<endl;// xor operator

    cout<<(4<<1)<<endl;//left shift formula(a*2^b) where a=4 n b=1
    cout<<(4>>1)<<endl;//right shift formula(a/2^b) where a=4 n b=1


}